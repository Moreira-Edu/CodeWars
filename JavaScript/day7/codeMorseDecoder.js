// In this kata you have to write a simple Morse code decoder. While the Morse code is now mostly superseded by voice and digital data communication channels, it still has its use in some applications around the world.
// The Morse code encodes every character as a sequence of "dots" and "dashes". For example, the letter A is coded as ·−, letter Q is coded as −−·−, and digit 1 is coded as ·−−−−. The Morse code is case-insensitive, traditionally capital letters are used. When the message is written in Morse code, a single space is used to separate the character codes and 3 spaces are used to separate words. For example, the message HEY JUDE in Morse code is ···· · −·−−   ·−−− ··− −·· ·.

// NOTE: Extra spaces before or after the code have no meaning and should be ignored.

// In addition to letters, digits and some punctuation, there are some special service codes, the most notorious of those is the international distress signal SOS (that was first issued by Titanic), that is coded as ···−−−···. These special codes are treated as single special characters, and usually are transmitted as separate words.

// Your task is to implement a function that would take the morse code as input and return a decoded human-readable string.

/**
 *
 * @param {string} morseCode
 * @returns {string}
 */
export function decodeMorse(morseCode) {
  const decoder = {
    A: /(?<=^)(\.-)(?=\s)|(?<=[^-|\.])(\.-)(?=[^-|\.])|(?<=\s)(\.-)(?=$)|(?<=^)(\.-)(?=$)/g,
    B: /(?<=^)(-\.\.\.)(?=\s)|(?<=[^-|\.])(-\.\.\.)(?=[^-|\.])|(?<=\s)(-\.\.\.)(?=$)|(?<=^)(-\.\.\.)(?=$)/g,
    C: /(?<=^)(-\.-\.)(?=\s)|(?<=[^-|\.])(-\.-\.)(?=[^-|\.])|(?<=\s)(-\.-\.)(?=$)|(?<=^)(-\.-\.)(?=$)/g,
    D: /(?<=^)(-\.\.)(?=\s)|(?<=[^-|\.])(-\.\.)(?=[^-|\.])|(?<=\s)(-\.\.)(?=$)|(?<=^)(-\.\.)(?=$)/g,
    E: /(?<=^)(\.)(?=\s)|(?<=[^-|\.])(\.)(?=[^-|\.])|(?<=\s)(\.)(?=$)|(?<=^)(\.)(?=$)/g,
    F: /(?<=^)(\.\.-\.)(?=\s)|(?<=[^-|\.])(\.\.-\.)(?=[^-|\.])|(?<=\s)(\.\.-\.)(?=$)|(?<=^)(\.\.-\.)(?=$)/g,
    G: /(?<=^)(--\.)(?=\s)|(?<=[^-|\.])(--\.)(?=[^-|\.])|(?<=\s)(--\.)(?=$)|(?<=^)(--\.)(?=$)/g,
    H: /(?<=^)(\.\.\.\.)(?=\s)|(?<=[^-|\.])(\.\.\.\.)(?=[^-|\.])|(?<=\s)(\.\.\.\.)(?=$)|(?<=^)(\.\.\.\.)(?=$)/g,
    I: /(?<=^)(\.\.)(?=\s)|(?<=[^-|\.])(\.\.)(?=[^-|\.])|(?<=\s)(\.\.)(?=$)|(?<=^)(\.\.)(?=$)/g,
    J: /(?<=^)(\.---)(?=\s)|(?<=[^-|\.])(\.---)(?=[^-|\.])|(?<=\s)(\.---)(?=$)|(?<=^)(\.---)(?=$)/g,
    K: /(?<=^)(-\.-)(?=\s)|(?<=[^-|\.])(-\.-)(?=[^-|\.])|(?<=\s)(-\.-)(?=$)|(?<=^)(-\.-)(?=$)/g,
    L: /(?<=^)(\.-\.\.)(?=\s)|(?<=[^-|\.])(\.-\.\.)(?=[^-|\.])|(?<=\s)(\.-\.\.)(?=$)|(?<=^)(\.-\.\.)(?=$)/g,
    M: /(?<=^)(--)(?=\s)|(?<=[^-|\.])(--)(?=[^-|\.])|(?<=\s)(--)(?=$)|(?<=^)(--)(?=$)/g,
    N: /(?<=^)(-\.)(?=\s)|(?<=[^-|\.])(-\.)(?=[^-|\.])|(?<=\s)(-\.)(?=$)|(?<=^)(-\.)(?=$)/g,
    O: /(?<=^)(---)(?=\s)|(?<=[^-|\.])(---)(?=[^-|\.])|(?<=\s)(---)(?=$)|(?<=^)(---)(?=$)/g,
    P: /(?<=^)(\.--\.)(?=\s)|(?<=[^-|\.])(\.--\.)(?=[^-|\.])|(?<=\s)(\.--\.)(?=$)|(?<=^)(\.--\.)(?=$)/g,
    Q: /(?<=^)(--\.-)(?=\s)|(?<=[^-|\.])(--\.-)(?=[^-|\.])|(?<=\s)(--\.-)(?=$)|(?<=^)(--\.-)(?=$)/g,
    R: /(?<=^)(\.-\.)(?=\s)|(?<=[^-|\.])(\.-\.)(?=[^-|\.])|(?<=\s)(\.-\.)(?=$)|(?<=^)(\.-\.)(?=$)/g,
    S: /(?<=^)(\.\.\.)(?=\s)|(?<=[^-|\.])(\.\.\.)(?=[^-|\.])|(?<=\s)(\.\.\.)(?=$)|(?<=^)(\.\.\.)(?=$)/g,
    T: /(?<=^)(-)(?=\s)|(?<=[^-|\.])(-)(?=[^-|\.])|(?<=\s)(-)(?=$)|(?<=^)(-)(?=$)/g,
    U: /(?<=^)(\.\.-)(?=\s)|(?<=[^-|\.])(\.\.-)(?=[^-|\.])|(?<=\s)(\.\.-)(?=$)|(?<=^)(\.\.-)(?=$)/g,
    V: /(?<=^)(\.\.\.-)(?=\s)|(?<=[^-|\.])(\.\.\.-)(?=[^-|\.])|(?<=\s)(\.\.\.-)(?=$)|(?<=^)(\.\.\.-)(?=$)/g,
    W: /(?<=^)(\.--)(?=\s)|(?<=[^-|\.])(\.--)(?=[^-|\.])|(?<=\s)(\.--)(?=$)|(?<=^)(\.--)(?=$)/g,
    X: /(?<=^)(-\.\.-)(?=\s)|(?<=[^-|\.])(-\.\.-)(?=[^-|\.])|(?<=\s)(-\.\.-)(?=$)|(?<=^)(-\.\.-)(?=$)/g,
    Y: /(?<=^)(-\.--)(?=\s)|(?<=[^-|\.])(-\.--)(?=[^-|\.])|(?<=\s)(-\.--)(?=$)|(?<=^)(-\.--)(?=$)/g,
    Z: /(?<=^)(--\.\.)(?=\s)|(?<=[^-|\.])(--\.\.)(?=[^-|\.])|(?<=\s)(--\.\.)(?=$)|(?<=^)(--\.\.)(?=$)/g,
    1: /(?<=^)(\.----)(?=\s)|(?<=[^-|\.])(\.----)(?=[^-|\.])|(?<=\s)(\.----)(?=$)|(?<=^)(\.----)(?=$)/g,
    2: /(?<=^)(\.\.---)(?=\s)|(?<=[^-|\.])(\.\.---)(?=[^-|\.])|(?<=\s)(\.\.---)(?=$)|(?<=^)(\.\.---)(?=$)/g,
    3: /(?<=^)(\.\.\.--)(?=\s)|(?<=[^-|\.])(\.\.\.--)(?=[^-|\.])|(?<=\s)(\.\.\.--)(?=$)|(?<=^)(\.\.\.--)(?=$)/g,
    4: /(?<=^)(\.\.\.\.-)(?=\s)|(?<=[^-|\.])(\.\.\.\.-)(?=[^-|\.])|(?<=\s)(\.\.\.\.-)(?=$)|(?<=^)(\.\.\.\.-)(?=$)/g,
    5: /(?<=^)(\.\.\.\.\.)(?=\s)|(?<=[^-|\.])(\.\.\.\.\.)(?=[^-|\.])|(?<=\s)(\.\.\.\.\.)(?=$)|(?<=^)(\.\.\.\.\.)(?=$)/g,
    6: /(?<=^)(-\.\.\.\.)(?=\s)|(?<=[^-|\.])(-\.\.\.\.)(?=[^-|\.])|(?<=\s)(-\.\.\.\.)(?=$)|(?<=^)(-\.\.\.\.)(?=$)/g,
    7: /(?<=^)(--\.\.\.)(?=\s)|(?<=[^-|\.])(--\.\.\.)(?=[^-|\.])|(?<=\s)(--\.\.\.)(?=$)|(?<=^)(--\.\.\.)(?=$)/g,
    8: /(?<=^)(---\.\.)(?=\s)|(?<=[^-|\.])(---\.\.)(?=[^-|\.])|(?<=\s)(---\.\.)(?=$)|(?<=^)(---\.\.)(?=$)/g,
    9: /(?<=^)(----\.)(?=\s)|(?<=[^-|\.])(----\.)(?=[^-|\.])|(?<=\s)(----\.)(?=$)|(?<=^)(----\.)(?=$)/g,
    0: /(?<=^)(-----)(?=\s)|(?<=[^-|\.])(-----)(?=[^-|\.])|(?<=\s)(-----)(?=$)|(?<=^)(-----)(?=$)/g,
    SOS: /(?<=^)(\.\.\.---\.\.\.)(?=\s)|(?<=[^-|\.])(\.\.\.---\.\.\.)(?=[^-|\.])|(?<=\s)(\.\.\.---\.\.\.)(?=$)|(?<=^)(\.\.\.---\.\.\.)(?=$)/g,
    "!": /(?<=^)(-\.-\.--)(?=\s)|(?<=[^-|\.])(-\.-\.--)(?=[^-|\.])|(?<=\s)(-\.-\.--)(?=$)|(?<=^)(-\.-\.--)(?=$)/g,
    ".": /(?<=^)(\.-\.-\.-)(?=\s)|(?<=[^-|\.])(\.-\.-\.-)(?=[^-|\.])|(?<=\s)(\.-\.-\.-)(?=$)|(?<=^)(\.-\.-\.-)(?=$)/g,
  };
  const singleSpace =
    /(?<=^)(\s)(?=[^\s])|(?<=[^\s])(\s)(?=[^\s])|(?<=[^\s])(\s)(?=$)/g;
  const multipleSpace = /\s{3}/g;
  const borderMultipleSpace = /^\s{2,9}|\s{2,9}$/g;

  Object.entries(decoder).forEach(([word, code]) => {
    morseCode = morseCode.replace(code, `${word}`);
  });

  morseCode = morseCode
    .replace(singleSpace, "")
    .replace(borderMultipleSpace, "")
    .replace(multipleSpace, " ");

  return morseCode;
}
